<?php 

class Information_acquired_thru_model extends CI_Model {

    protected $table_name = "information_acquired_thru";

    public function get_all()
    {
        $this->db->order_by('id','ASC');
        $query = $this->db
            ->get($this->table_name);
        if($query->num_rows() > 0){
            return $query->result();
        }
        return [];
    }
}